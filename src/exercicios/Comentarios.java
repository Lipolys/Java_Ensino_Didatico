package exercicios;

/**Exemplo de Comentário:
 * Essa classe demonstra a utilização de variáveis em uma classe
 * em Java. São declaradas variáveis tipos int e double.
 * O exemplo também demonstra como imprimir o conteúdo das variáveis na tela.
 */
public class Comentarios
{
    public static void main (String[] args)
    {
        int matricula = 1089, idade = 20; //Declaração de variáveis do tipo inteiro
        double dolar = 2.62;
        /* As linhas a seguir enviam o conteúdo
        das variáveis para a tela do usuário.
         */
        System.out.println("Matrícula:" + matricula + ", idade:" + idade + ", dolar:" + dolar);
    }
}
